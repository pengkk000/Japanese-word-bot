import requests
from bs4 import BeautifulSoup
from pykakasi import kakasi

# -------------------------
# 일본어 → 로마자
# -------------------------

kks = kakasi()

def to_romaji(text):
    return "".join(item["hepburn"] for item in kks.convert(text))


